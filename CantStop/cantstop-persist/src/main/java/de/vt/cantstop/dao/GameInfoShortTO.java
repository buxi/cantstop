package de.vt.cantstop.dao;

/**
 * TransferObject to hold short database information about the game
 * @author buxi
 *
 */
public class GameInfoShortTO {
	private String description;
	private int gameId;
	
	/**
	 * @return the description
	 */
	public String getDescription() {
		return description;
	}
	/**
	 * @return the gameId
	 */
	public int getGameId() {
		return gameId;
	}
	
	
}
